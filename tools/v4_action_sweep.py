#!/usr/bin/env python
"""CSC-v4 module A5 — offline counterfactual action-gain labels (the action-utility
supervision target for the V4 model's `action_utility` head).

For a PASSIVE run dir (predictions/ + states/ + telemetry/, as produced by
tools/run_with_csc.py) we walk each sequence and, for every frame inside a
loss/risk WINDOW, ESTIMATE what each control Action (v4.types.Action) would have
done to the NEXT-frame IoU vs the passive baseline, using only stored
predictions + ground-truth + telemetry. We emit, per windowed frame:

    action_gain[action_name] = ΔIoU  (estimated next-frame IoU minus passive)
    best_action               = argmax-gain action name
    recoverable_by_bridge     = motion_bridge gain >= recover_margin
    recoverable_by_relocate   = relocate gain      >= recover_margin
    needs_global_search       = only global_search clears recover_margin
    do_not_act                = every action gain <= 0 (HOLD is best)
    template_update_safe      = passive box still on target (safe to refresh)

────────────────────────────────────────────────────────────────────────────
APPROXIMATION (the whole sweep is offline; NO tracker is re-run — see A5 in
csc_lib/csc/v4/CONTRACT.md, "You may APPROXIMATE the sweep cheaply offline
WITHOUT re-running the tracker"). A full tracker-in-the-loop sweep is future
work. Every approximate quantity below is marked `# APPROX`. The stored
telemetry does NOT contain candidate pixel boxes — only scalar score-map stats
(sm_n_secondary, sm_local_top2_ratio, sm_peak_distance, ...) — so RELOCATE and
GLOBAL_SEARCH are modelled from those scalars + a GT-derived "best achievable"
box rather than from real candidate coordinates. These are SUPERVISION
PROXIES, not measured tracker outputs, and must be regenerated by a real sweep
before any headline claim.

Per-action next-frame-IoU proxy (all `# APPROX`):
  HOLD            : passive next-frame IoU (the box the tracker actually drew).
  MOTION_BRIDGE   : IoU(extrapolated-centre box, GT_next). The centre is
                    last_good_centre + EMA_velocity * frames_since_loss with the
                    last-good size, mirroring run_with_csc.py's motion_bridge
                    (regularity gate + displacement cap). Smooth motion -> high.
  RELOCATE        : IoU of a "secondary-peak-on-target" box, CREDITED only when
                    telemetry says a usable secondary peak exists
                    (sm_n_secondary>0 OR sm_local_top2_ratio>=relocate_ratio).
                    Proxy box = GT_next-snapped box at the passive scale (best
                    case a real secondary peak could achieve), else passive box.
  WIDEN           : passive box nudged a fraction (widen_frac) toward the
                    GT-snapped box — a wider crop recovers a target that drifted
                    just outside the current crop; bounded, never worse than HOLD.
  GLOBAL_SEARCH   : GT-snapped-box IoU (a budgeted full re-detector can in
                    principle re-acquire anywhere) — credited only on a genuine
                    loss (passive IoU < loss_iou) so easy frames don't reward it.
  TEMPLATE_UPDATE : position-neutral this frame == HOLD IoU (it changes future
                    template quality, not next-frame localisation).
  FREEZE          : position-neutral this frame == HOLD IoU.
────────────────────────────────────────────────────────────────────────────

CLI:
  --passive_dir <run>   passive run dir (has predictions/ states/ telemetry/)
  --dataset uav123      dataset name for la_smoke.build_index (default uav123)
  --out <path.jsonl>    output JSONL (one record per windowed frame)
  --seqs a b ...        restrict to these sequences (optional)
  --selftest            run a tiny synthetic example with NO dataset; assert
                        the output schema; write nothing unless --out given.

Reuses tools/la_smoke.py loaders (build_index, seq_iou, gt_array, read_preds).
Read-only over the run dir; offline SOT benchmark; CPU-only; additive.
"""
