_N_TRAIN = 98
_N_VAL = 12
# test = everything after train + val


# ---------------------------------------------------------------------------
# Dataset class
# ---------------------------------------------------------------------------


@DATASETS.register("uav123_train")
@DATASETS.register("uav123_val")
@DATASETS.register("uav123_test")
class UAV123MLDataset:
    """UAV123 with per-frame difficulty labels for ML training (Phase 11).

    Parameters
    ----------
    root:
        UAV123 dataset root (same semantics as ``UAV123Dataset``).
        Defaults to ``$UAV_DATA_ROOT/uav123/UAV123/``.
    split:
        One of ``"train"``, ``"val"``, or ``"test"``.
    labels_cache:
        Path to the NPZ file generated by ``scripts/generate_ml_labels.py``.
        If ``None``, ``scene_class`` is set to ``CLEAR`` (0) for every frame
        and ``flow_features`` is all-zeros — useful for shape-contract tests.
    transform:
        Optional callable applied to the 128×128 numpy patch *before*
        conversion to tensor.  Signature: ``patch: np.ndarray → np.ndarray``.
    sample_interval:
        Subsample every N-th frame within each sequence (default 1 = all
        frames). Useful for faster training epochs.
    """

    # -----------------------------------------------------------------------
    # Class-level split constants (ADR-0011)
    # -----------------------------------------------------------------------

    SPLIT_TRAIN: list[str] = _ALL_SEQS_SORTED[:_N_TRAIN]
    SPLIT_VAL: list[str] = _ALL_SEQS_SORTED[_N_TRAIN : _N_TRAIN + _N_VAL]
    SPLIT_TEST: list[str] = _ALL_SEQS_SORTED[_N_TRAIN + _N_VAL :]

    # -----------------------------------------------------------------------
    # Construction
    # -----------------------------------------------------------------------

    def __init__(
        self,
        root: Path | str | None = None,
        split: Literal["train", "val", "test"] = "train",
        labels_cache: Path | str | None = None,
        transform: Callable[[np.ndarray], np.ndarray] | None = None,
        sample_interval: int = 1,
    ) -> None:
        if split not in ("train", "val", "test"):
            raise ValueError(f"split must be 'train', 'val', or 'test'; got {split!r}")
        self.split = split
        self.transform = transform
        self.sample_interval = max(1, int(sample_interval))

        # Select the sequence names for this split.
        split_map: dict[str, list[str]] = {
            "train": self.SPLIT_TRAIN,
            "val": self.SPLIT_VAL,
            "test": self.SPLIT_TEST,
        }
        self._split_seqs: list[str] = split_map[split]

        # Build the underlying UAV123 dataset for frame + annotation access.
        self._uav_ds = UAV123Dataset(root=root, split="test")

        # Load labels cache (NPZ).
        self._labels: dict[str, np.ndarray] | None = None
        self._flow_feats: dict[str, np.ndarray] | None = None
        if labels_cache is not None:
            self._load_labels(Path(labels_cache))

        # Build the flat index: list of (seq_name, frame_idx_in_seq) tuples.
        self._index: list[tuple[str, int]] = []
        self._seq_data: dict[str, dict[str, Any]] = {}  # seq metadata cache
        self._build_index()

    # -----------------------------------------------------------------------
    # Index construction
    # -----------------------------------------------------------------------

    def _load_labels(self, path: Path) -> None:
        """Load scene_class and flow_features arrays from the NPZ cache."""
        if not path.exists():
            _log.warning("UAV123MLDataset: labels_cache not found at %s — using zeros", path)
            return
        try:
            data = np.load(path, allow_pickle=True)
            self._labels = {}
            self._flow_feats = {}
            for key in data.files:
                parts = key.split("/", 1)
                if len(parts) == 2:
                    kind, seq_name = parts
                    if kind == "scene_class":
                        self._labels[seq_name] = data[key].astype(np.uint8)
                    elif kind == "flow_features":
                        self._flow_feats[seq_name] = data[key].astype(np.float32)
        except Exception as exc:
            _log.warning("UAV123MLDataset: failed to load labels from %s: %s", path, exc)
            self._labels = None
            self._flow_feats = None

    def _build_index(self) -> None:
        """Build flat frame index by iterating the underlying UAV123 dataset.

        Only sequences that appear in this split's name list are included.
        Frames are subsampled by ``sample_interval``.

        We iterate the dataset once here to discover which sequences
        actually exist on disk (the UAV123 loader skips missing/broken
        sequences).  Metadata (ground_truth, frame count) is cached in
        ``self._seq_data`` to avoid re-iterating on ``__getitem__`` calls.
        """
        split_set = set(self._split_seqs)
        for seq in self._uav_ds:
            if seq.name not in split_set:
                continue
            gt = seq.ground_truth
            n_frames = len(gt)
            if n_frames == 0:
                continue

            # Load all frames into RAM once (sequences can be long; we store
            # paths lazily via the frame dir instead).  We cache the sequence
            # object so we can re-read frames in __getitem__.
            self._seq_data[seq.name] = {
                "seq": seq,
                "n_frames": n_frames,
            }

            # Add (seq_name, frame_idx) pairs for subsampled frames.
            for fi in range(0, n_frames, self.sample_interval):
                self._index.append((seq.name, fi))

    # -----------------------------------------------------------------------
    # Dataset interface
    # -----------------------------------------------------------------------

    def __len__(self) -> int:
        return len(self._index)

    def __getitem__(self, idx: int) -> dict[str, Any]:
        """Return a per-frame training sample.

        Returns
        -------
        dict with keys:
          ``frame_patch``:    ``torch.Tensor`` shape ``[3, 128, 128]`` float32,
                              normalised to ``[0, 1]``.
          ``flow_features``:  ``np.ndarray`` shape ``[32]`` float32.
          ``scene_class``:    int — scene difficulty label (0–5).
          ``sequence_name``:  str.
          ``frame_idx``:      int — zero-based frame index within the sequence.
        """
        if idx < 0 or idx >= len(self._index):
            raise IndexError(f"index {idx} out of range for dataset of size {len(self._index)}")

        seq_name, frame_idx = self._index[idx]
        seq_data = self._seq_data[seq_name]
        seq = seq_data["seq"]
        gt = seq.ground_truth

        # --- extract frame patch ---
        patch = self._get_frame_patch(seq, frame_idx, gt[frame_idx])

        # --- apply optional user transform ---
        if self.transform is not None:
            patch = self.transform(patch)

        # --- convert to tensor ---
        if _TORCH_AVAILABLE:
            # patch is HWC uint8 or float; normalise to [0,1] float32 CHW.
            if patch.dtype == np.uint8:
                patch_f = patch.astype(np.float32) / 255.0
            else:
                patch_f = patch.astype(np.float32)
                if patch_f.max() > 1.0:
                    patch_f /= 255.0
            tensor = torch.from_numpy(patch_f.transpose(2, 0, 1))  # CHW
        else:  # pragma: no cover
            tensor = patch  # type: ignore[assignment]

        # --- scene class ---
        scene_class = 0
        if self._labels is not None and seq_name in self._labels:
            arr = self._labels[seq_name]
            if frame_idx < len(arr):
                scene_class = int(arr[frame_idx])

        # --- flow features ---
        flow_feats = np.zeros(32, dtype=np.float32)
        if self._flow_feats is not None and seq_name in self._flow_feats:
            arr = self._flow_feats[seq_name]
            if frame_idx < arr.shape[0]:
                row = arr[frame_idx]
                n = min(32, len(row))
                flow_feats[:n] = row[:n]

        return {
            "frame_patch": tensor,
            "flow_features": flow_feats,
            "scene_class": scene_class,
            "sequence_name": seq_name,
            "frame_idx": frame_idx,
        }

    # -----------------------------------------------------------------------
    # Patch extraction
    # -----------------------------------------------------------------------

    @staticmethod
    def _get_frame_patch(seq: Any, frame_idx: int, bbox: Any) -> np.ndarray:
        """Extract a 128×128 RGB patch centred on *bbox* with 2× context scale.

        Parameters
        ----------
        seq:
            A ``_UAV123Sequence`` (or any Sequence-protocol object).
        frame_idx:
            Zero-based index of the target frame within the sequence.
        bbox:
            Ground-truth bounding box for this frame (``BBox`` with x,y,w,h).

        Returns
        -------
        np.ndarray of shape ``(128, 128, 3)`` uint8 in RGB channel order.
        """
        # Read the specific frame by iterating up to frame_idx.
        frame: np.ndarray | None = None
        for fi, f in enumerate(seq.frames):
            if fi == frame_idx:
                frame = f
                break

        if frame is None:
            return np.zeros((128, 128, 3), dtype=np.uint8)

        fH, fW = frame.shape[:2]

        # Centre on bbox and apply 2× context scale.
        cx = bbox.x + bbox.w / 2.0
        cy = bbox.y + bbox.h / 2.0
        ctx_w = bbox.w * 2.0
        ctx_h = bbox.h * 2.0

        x1 = int(round(cx - ctx_w / 2.0))
        y1 = int(round(cy - ctx_h / 2.0))
        x2 = int(round(cx + ctx_w / 2.0))
        y2 = int(round(cy + ctx_h / 2.0))

        pad_l = max(0, -x1)
        pad_t = max(0, -y1)
        pad_r = max(0, x2 - fW)
        pad_b = max(0, y2 - fH)

        x1c, y1c = max(0, x1), max(0, y1)
        x2c, y2c = min(fW, x2), min(fH, y2)

        if x2c <= x1c or y2c <= y1c:
            return np.zeros((128, 128, 3), dtype=np.uint8)

        crop = frame[y1c:y2c, x1c:x2c]
        if pad_l > 0 or pad_t > 0 or pad_r > 0 or pad_b > 0:
            crop = cv2.copyMakeBorder(
                crop, pad_t, pad_b, pad_l, pad_r, cv2.BORDER_REPLICATE
            )

        patch = cv2.resize(crop, (128, 128), interpolation=cv2.INTER_LINEAR)

        # Convert BGR → RGB (UAV123 frames are loaded as BGR by OpenCV).
        patch = cv2.cvtColor(patch, cv2.COLOR_BGR2RGB)
        return patch


