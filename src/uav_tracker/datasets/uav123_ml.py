"""UAV123 ML Dataset — per-frame difficulty labels for supervised training (Phase 11).

Extends UAV123Dataset with pre-computed scene class labels.  Labels are loaded
from a cache NPZ file generated by ``scripts/generate_ml_labels.py``.

Split strategy (ADR-0011)
--------------------------
Train / Val / Test = 80 / 10 / 10 by *alphabetical sequence name*.
All 118 sequences listed in the UAV123 task corpus are sorted
lexicographically; the first 98 form train, the next 12 val, the last 8 test.
Splits are fixed as class constants — never computed at runtime from the
filesystem — so they survive dataset-root changes.

Scene classes (ADR-0012)
------------------------
  0  CLEAR        — no difficulty signals
  1  MODERATE     — entropy H̄ > 0.5 for ≥ 5 consecutive frames
  2  CHALLENGING  — IoU < 0.5 for ≥ 3 consecutive frames
  3  RISK_LOSS    — IoU < 0.2 for ≥ 2 consecutive frames
  4  RECOVERY     — tracker ``status == "lost"`` for ≥ 10 consecutive frames
  5  LOW_RES      — bbox area < 400 px²

Priority (highest wins when multiple rules fire):
  RECOVERY > RISK_LOSS > CHALLENGING > LOW_RES > MODERATE > CLEAR
"""

from __future__ import annotations

import logging
from pathlib import Path
from typing import Any, Callable, Literal

import cv2
import numpy as np

try:
    import torch
    _TORCH_AVAILABLE = True
except ImportError:  # pragma: no cover
    _TORCH_AVAILABLE = False

from uav_tracker.registry import DATASETS
from uav_tracker.datasets.uav123 import UAV123Dataset

_log = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Scene class constants
# ---------------------------------------------------------------------------

SCENE_CLEAR = 0
SCENE_MODERATE = 1
SCENE_CHALLENGING = 2
SCENE_RISK_LOSS = 3
SCENE_RECOVERY = 4
SCENE_LOW_RES = 5

